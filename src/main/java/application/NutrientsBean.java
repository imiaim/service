package application;


public class NutrientsBean {
    private float fats;
    private float proteine;
    private float carbonate;
    private float kkal;

    public NutrientsBean() {
    }

    public float getFats() {
        return fats;
    }

    public void setFats(float fats) {
        this.fats = fats;
    }

    public float getProteine() {
        return proteine;
    }

    public void setProteine(float proteine) {
        this.proteine = proteine;
    }

    public float getCarbonate() {
        return carbonate;
    }

    public void setCarbonate(float carbonate) {
        this.carbonate = carbonate;
    }

    public float getKkal() {
        return kkal;
    }

    public void setKkal(float kkal) {
        this.kkal = kkal;
    }
}
